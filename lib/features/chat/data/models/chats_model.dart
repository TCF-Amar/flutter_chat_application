// chats/{chatId}/messages/{messageId}
import 'package:chat_kare/features/chat/domain/entities/chats_entity.dart';
import 'package:cloud_firestore/cloud_firestore.dart';

class ChatsModel extends ChatsEntity {
  ChatsModel({
    required super.id,
    required super.chatId,
    required super.senderId,
    required super.receiverId,
    required super.senderName,
    required super.receiverName,
    super.senderPhotoUrl,
    super.receiverPhotoUrl,
    required super.text,
    super.type = MessageType.text,
    required super.timestamp,
    super.isRead = false,
    super.readBy = const [],
    super.mediaUrl,
    super.mediaSize,
    super.status,
    super.isEdited = false,
    super.replyToMessageId,
    super.replyToSenderName,
    super.replyToText,
    super.replyToType,
    super.replyToMediaUrl,
    super.deletedBy,
    super.isDeletedForEveryone,
    super.uploadProgress,
    super.localFilePath,
    super.uploadError,
    super.videoUrl,
    super.documentName,
  });

  factory ChatsModel.fromJson(Map<String, dynamic> json) {
    return ChatsModel(
      id: json["id"],
      chatId: json["chatId"],
      senderId: json["senderId"],
      receiverId: json["receiverId"],
      senderName: json["senderName"] ?? 'Unknown',
      receiverName: json["receiverName"] ?? 'Unknown',
      senderPhotoUrl: json["senderPhotoUrl"],
      receiverPhotoUrl: json["receiverPhotoUrl"],
      text: json["text"],
      type: MessageType.values.firstWhere((e) => e.name == json["type"]),
      timestamp: (json["timestamp"] as Timestamp).toDate(),
      isRead: json["isRead"],
      readBy: List<String>.from(json["readBy"]),
      mediaUrl: json["mediaUrl"],
      mediaSize: json["mediaSize"],
      status: MessageStatus.sent,
      isEdited: json["isEdited"] ?? false,
      replyToMessageId: json["replyToMessageId"],
      replyToSenderName: json["replyToSenderName"],
      replyToText: json["replyToText"],
      replyToType: json["replyToType"] != null
          ? MessageType.values.firstWhere((e) => e.name == json["replyToType"])
          : null,
      replyToMediaUrl: json["replyToMediaUrl"],
      deletedBy: List<String>.from(json["deletedBy"] ?? []),
      isDeletedForEveryone: json["isDeletedForEveryone"] ?? false,
      // Upload state fields are local-only, not stored in Firestore
      uploadProgress: 0.0,
      localFilePath: null,
      uploadError: null,
      videoUrl: json["videoUrl"],
      documentName: json["documentName"],
    );
  }

  Map<String, dynamic> toJson() {
    return {
      "id": id,
      "chatId": chatId,
      "senderId": senderId,
      "receiverId": receiverId,
      "senderName": senderName,
      "receiverName": receiverName,
      "senderPhotoUrl": senderPhotoUrl,
      "receiverPhotoUrl": receiverPhotoUrl,
      "text": text,
      "type": type.name,
      "timestamp": timestamp,
      "isRead": isRead,
      "readBy": readBy,
      "mediaUrl": mediaUrl,
      "mediaSize": mediaSize,
      "isEdited": isEdited,
      "replyToMessageId": replyToMessageId,
      "replyToSenderName": replyToSenderName,
      "replyToText": replyToText,
      "replyToType": replyToType?.name,
      "replyToMediaUrl": replyToMediaUrl,
      "deletedBy": deletedBy,
      "isDeletedForEveryone": isDeletedForEveryone,
      "videoUrl": videoUrl,
      "documentName": documentName,
      // Note: uploadProgress, localFilePath, uploadError are NOT included
      // They are local-only fields for UI state management
    };
  }

  ChatsEntity toEntity() {
    return ChatsEntity(
      id: id,
      chatId: chatId,
      senderId: senderId,
      receiverId: receiverId,
      senderName: senderName,
      receiverName: receiverName,
      senderPhotoUrl: senderPhotoUrl,
      receiverPhotoUrl: receiverPhotoUrl,
      text: text,
      type: type,
      timestamp: timestamp,
      isRead: isRead,
      readBy: readBy,
      mediaUrl: mediaUrl,
      mediaSize: mediaSize,
      status: status,
      isEdited: isEdited,
      replyToMessageId: replyToMessageId,
      replyToSenderName: replyToSenderName,
      replyToText: replyToText,
      replyToType: replyToType,
      replyToMediaUrl: replyToMediaUrl,
      deletedBy: deletedBy,
      isDeletedForEveryone: isDeletedForEveryone,
      uploadProgress: uploadProgress,
      localFilePath: localFilePath,
      uploadError: uploadError,
      videoUrl: videoUrl,
      documentName: documentName,
    );
  }

  // form entity
  factory ChatsModel.fromEntity(ChatsEntity entity) {
    return ChatsModel(
      id: entity.id,
      chatId: entity.chatId,
      senderId: entity.senderId,
      receiverId: entity.receiverId,
      senderName: entity.senderName,
      receiverName: entity.receiverName,
      senderPhotoUrl: entity.senderPhotoUrl,
      receiverPhotoUrl: entity.receiverPhotoUrl,
      text: entity.text,
      type: entity.type,
      timestamp: entity.timestamp,
      isRead: entity.isRead,
      readBy: entity.readBy,
      mediaUrl: entity.mediaUrl,
      mediaSize: entity.mediaSize,
      status: entity.status,
      isEdited: entity.isEdited,
      replyToMessageId: entity.replyToMessageId,
      replyToSenderName: entity.replyToSenderName,
      replyToText: entity.replyToText,
      replyToType: entity.replyToType,
      replyToMediaUrl: entity.replyToMediaUrl,
      deletedBy: entity.deletedBy,
      isDeletedForEveryone: entity.isDeletedForEveryone,
      uploadProgress: entity.uploadProgress,
      localFilePath: entity.localFilePath,
      uploadError: entity.uploadError,
      videoUrl: entity.videoUrl,
      documentName: entity.documentName,
    );
  }
}
